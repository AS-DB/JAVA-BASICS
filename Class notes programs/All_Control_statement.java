class All_Control_statement{
    public static void main(String args[]){
        int i;
        //Do while loop
        i=2;
        do{
            System.out.println("\t"+i);
            i++;

        }while (i<=12);

        //while 
        i=22;
        while (i<=72){
            System.out.println("\t"+i);
            i++;
        }
        //for 
        for(i=0;i<4;i++){
            System.out.println("\t"+i);
        }
    }
}