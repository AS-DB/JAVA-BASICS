class For_each{
    public static void main(String[] args) {
        
        String[] birds= {"pigeon","parrot","owl","crow"};

        for(String i : birds){
            System.out.println(i);
        }

        int[] age= {1,2,3,4};
        for(int j: age){
            System.out.println(j);

        }
    }
}