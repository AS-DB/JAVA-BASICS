class Swapping{
    public static void main(String args[]){
        //temporary variable swap
        int x=3,y=2,v;
        v=x;
        x=y;
        y=v;
        System.out.println("x:"+x+"y:"+y);
        
        //direct swap
        int a=32,b=30;
        System.out.println("a\t"+b+"b\t"+a);
    }
}